		var nowList=lists[i];
		// è·å¾—æ¯ç»„å¯¹åº”çš„æ•°æ®
		var now_list=lists[i].user_list;
		// æ¯ç»„æ•°æ®çš„length,åˆ†é¡µç”¨çš„åˆ°
		var innerLength=now_list.length;
		//2.3 å¦‚æœæœ‰
