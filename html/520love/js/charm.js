		var nowList=newLists[k];
		// å¦‚æœå¤šäº10ä¸ªçš„äºŒç»´æ•°ç»„,ç”¨ä»¥ç‚¹å‡»æ¢ä¸€æ‰¹
		var outerLists=JSON.stringify(newLists[k].outer);
