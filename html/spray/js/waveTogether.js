				str_html+='<img src="'+list_headimg+'" alt="">';
				str_html+='</div>';
				str_html+='<p>'+list_nickname+'</p>';
				if(follow_status==0){
					str_html+='<i class="follow" data-pfid="'+list_pfid+'"></i>';
				}
				str_html+='</div>';
				str_html+='<div class="user-content">';
				if(has_img==1){
					str_html+='<div class="user-msg has-img">';
				}else if(has_img==0){
					str_html+='<div class="user-msg">';
				}
